export function createTranslator(language) {
    return function translate(key, vars = {}) {
        const template = UI_STRINGS[language]?.[key] ?? UI_STRINGS.zh[key] ?? key;
        return template.replace(/\{(\w+)\}/g, (_, name) => vars[name] ?? '');
    };
}